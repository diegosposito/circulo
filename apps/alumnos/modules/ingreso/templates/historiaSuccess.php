					<h2>CIRCULO ODONTOLOGICO Concepción del Uruguay</h2>
					<h2>Historia</h2>
					<p>Con la búsqueda de desarrollar la acción gremial, científica, cultural y social, teniendo por objetivo la dignificación de los <strong>Profesionales Odontólogos del Departamento Uruguay</strong>, en el año 1957 se constituye el <strong>Círculo Odontológico.</strong></p>
					<p><strong>Acta Nro 1</strong></p>

                    <p>En la Ciudad de <strong>Concepción del Uruguay</strong>, a los 12 días del mes de marzo de 1957, queda reorganizado el <strong>Círculo Odontológico de Concepción del Uruguay (C.O.C.U.).</strong> Redactado y aprobado su estatuto y Comisión Directiva, por voto de la mayoría de los Socios que firman el Acta, quedando formada de la siguiente manera:
					<br><br><strong>Presidente:</strong> Dr. Juan José Rosso
					<br><strong>Secretario:</strong> Dr. René Punzi
					<br><strong>Tesorero:</strong> Dr. Héctor Palma Parodi
					<br><strong>1º Vocal:</strong> Dr. Máximo Alberto Schmidt
					<br><strong>2º Vocal:</strong> Dr. Julio Artusi
					<br>
					<p><strong>Firman el acta los siguientes Socios:</strong></p>
                    <p>Juan José Rosso – René Punzi – Héctor Palma Parodi – Máximo Alberto Schmidt – Julio Artusi – Dora Santini – Carlos María Bernasconi – Francisco Nano – Rodolfo Rodríguez Decurgez – Arnoldo Erramuspe – Raúl Erramuspe Horacio – Jacobo Kuperman – Dionisio Nano. </p>
